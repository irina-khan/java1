public class positive_negative{
    public static void main(String[] args) {
        //объявляем и инициализируем переменную для проверки
        int A=1;
        //проверяем переменную на положительность/отрицательность (A=0 считаем положительной)
        if (A>=0) {
            System.out.println("'A' is positive");
        } else {
            System.out.println("'A' is negative");
        }
    }
}